/**
 * List of possible screen origin positions.
 * 
 * @author Marshall Brain
 *
 */
public enum ScreenPosition {
	
	/**
	 * The origin is in the center of the screen.
	 */
	CENTER,
	/**
	 * The origin is in the upper left hand corner of the screen
	 */
	ZERO;

}
